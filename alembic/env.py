def include_object(object, name, type_, reflected, compare_to):
    """
    Filter function to only track objects in the 'test' schema.
    This prevents Alembic from detecting tables in other schemas as removed.
    """
    # Only include objects in the test schema
    if type_ == "table":
        # If the table has a schema attribute, check if it's "test"
        if hasattr(object, "schema"):
            return object.schema == "test"
        # If no schema is specified in the object, it's likely in public schema - skip it
        return False

    # For indexes and other objects, check if they belong to a table in test schema
    if hasattr(object, "table") and hasattr(object.table, "schema"):
        return object.table.schema == "test"

    # Skip version table comparisons from other schemas
    if type_ == "table" and name == "alembic_version":
        return False

    return True


def run_migrations_offline() -> None:
    """Run migrations in 'offline' mode.

    This configures the context with just a URL
    and not an Engine, though an Engine is acceptable
    here as well.  By skipping the Engine creation
    we don't even need a DBAPI to be available.

    Calls to context.execute() here emit the given string to the
    script output.

    """
    url = config.get_main_option("sqlalchemy.url")
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
        include_schemas=True,
        version_table_schema="test",
        include_object=include_object,
    )

    with context.begin_transaction():
        context.run_migrations()


def run_migrations_online() -> None:
    """Run migrations in 'online' mode.

    In this scenario we need to create an Engine
    and associate a connection with the context.

    """
    connectable = engine_from_config(
        config.get_section(config.config_ini_section, {}),
        prefix="sqlalchemy.",
        poolclass=pool.NullPool,
    )

    with connectable.connect() as connection:
        context.configure(
            connection=connection,
            target_metadata=target_metadata,
            include_schemas=True,
            version_table_schema="test",
            include_object=include_object,
        )

        with context.begin_transaction():
            context.run_migrations()


if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()
